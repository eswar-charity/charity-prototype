/**
 * Copies unique images from event-data/ → public/events/.
 * Deduplicates by file content hash; converts HEIC/HEIF → JPEG.
 * Writes src/data/eventPhotoManifest.js with exact photo counts.
 */
import { createHash } from 'node:crypto';
import { cpSync, existsSync, mkdirSync, readFileSync, readdirSync, rmSync, statSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import convert from 'heic-convert';

const __dirname = dirname(fileURLToPath(import.meta.url));
const appRoot = join(__dirname, '..');
const repoRoot = join(appRoot, '..');
const eventDataRoot = join(repoRoot, 'event-data');
const publicRoot = join(appRoot, 'public', 'events');
const manifestPath = join(appRoot, 'src', 'data', 'eventPhotoManifest.js');

const FOLDER_MAP = {
  'neon-night': 'Neon Night',
  'give-now': "Give Now Apre' Later",
  'golf-outing': 'Charity Hub Golf Outing',
  'dog-dad': 'Dog Dad',
  'breakneck-ridge-run': 'Breakneck Ridge Run',
};

const IMAGE_RE = /\.(jpe?g|png|heic|heif)$/i;

function listImages(dir) {
  if (!existsSync(dir)) return [];
  return readdirSync(dir)
    .filter((f) => IMAGE_RE.test(f) && !f.startsWith('.'))
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));
}

function contentHash(buf) {
  return createHash('sha1').update(buf).digest('hex');
}

function outExtFor(file) {
  if (/\.png$/i.test(file)) return 'png';
  return 'jpg';
}

async function toWebBuffer(srcPath, file) {
  const input = readFileSync(srcPath);
  if (/\.(heic|heif)$/i.test(file)) {
    const output = await convert({ buffer: input, format: 'JPEG', quality: 0.9 });
    return { buffer: Buffer.from(output), hash: contentHash(input), action: 'converted HEIC→JPEG' };
  }
  return { buffer: input, hash: contentHash(input), action: 'copied' };
}

if (!existsSync(eventDataRoot)) {
  console.error(`event-data folder not found at ${eventDataRoot}`);
  process.exit(1);
}

mkdirSync(publicRoot, { recursive: true });

const manifest = {};

for (const [key, folderName] of Object.entries(FOLDER_MAP)) {
  const srcDir = join(eventDataRoot, folderName, folderName);
  const destDir = join(publicRoot, key);

  if (!existsSync(srcDir)) {
    console.warn(`Skipping ${key}: source not found at ${srcDir}`);
    continue;
  }

  mkdirSync(destDir, { recursive: true });
  for (const f of readdirSync(destDir)) {
    const p = join(destDir, f);
    if (statSync(p).isFile()) rmSync(p);
  }

  const files = listImages(srcDir);
  const seen = new Set();
  const photoExt = {};
  let i = 0;

  for (const file of files) {
    const { buffer, hash, action } = await toWebBuffer(join(srcDir, file), file);
    if (seen.has(hash)) {
      console.log(`  ${key}: skip duplicate ${file}`);
      continue;
    }
    seen.add(hash);
    i += 1;
    const ext = outExtFor(file);
    writeFileSync(join(destDir, `img${i}.${ext}`), buffer);
    photoExt[i] = ext;
    console.log(`  ${key}/img${i}.${ext} ← ${file} (${action})`);
  }

  manifest[key] = { photoCount: i, photoExt };
  console.log(`${key}: ${i} unique image(s)`);
}

const manifestSrc = `/** Auto-generated by scripts/sync-event-assets.mjs — do not edit by hand. */\nexport const EVENT_PHOTO_MANIFEST = ${JSON.stringify(manifest, null, 2)};\n`;
writeFileSync(manifestPath, manifestSrc);
console.log(`Wrote ${manifestPath}`);
